/*CREACION DE TABLA MATRICULA*/
CREATE TABLE IF NOT EXISTS MATRICULA (FECHA DATE NOT NULL, PAGADO BOOL NOT NULL, ATTRIBUTE INT NOT NULL, MODOPAGO VARCHAR(45) NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL, 
CONSTRAINT CURSO_PROPIO_ID_PK PRIMARY KEY (ID),
CONSTRAINT ESTUDIANTE_DNI_PK PRIMARY KEY(DNI), 
CONSTRAINT CURSO_PROPIO_FK FOREIGN KEY(CURSOPROPIO_ID) REFERENCES CURSOPROPIO (ID) ON DELETE NO ACTION, 
CONSTRAINT ESTUDIANTE_FK FOREIGN KEY (ESTUDIANTE_DNI) REFERENCES ESTUDIANTE (DNI) ON DELETE NO ACTION);

/*CREACION DE TABLA CURSOPROPIO*/
CREATE TABLE IF NOT EXISTS CURSOPROPIO (ID INT NOT NULL, NOMBRE VARCHAR(45) NOT NULL, ECTS INT NOT NULL, FECHAINICIO DATE NOT NULL, FECHAFIN DATE NOT NULL, TASAMATRICULA DOUBLE NOT NULL, EDICION INT NOT NULL, ESTADOCURSO BOOL NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL,  
CONSTRAINT ID_PK PRIMARY KEY (ID),
CONSTRAINT CENTRO_PROPIO_FK FOREIGN KEY(CENTRO_NOMBRE) REFERENCES CENTRO (NOMBRE) ON DELETE NO ACTION, 
CONSTRAINT SECRETARIO_PROFESOR_UCLM_FK FOREIGN KEY (SECRETARIO_PROFESOR_DNI) REFERENCES PROFESORUCLM (PROFESOR_DNI_FK) ON DELETE NO ACTION,
CONSTRAINT DIRECTOR_PROFESOR_UCLM_FK FOREIGN KEY (DIRECTOR_PROFESOR_DNI) REFERENCES PROFESORUCLM (PROFESOR_DNI_FK) ON DELETE NO ACTION);

/*CREACION DE TABLA MATERIA*/
CREATE TABLE IF NOT EXISTS MATERIA (NOMBRE VARCHAR(45) NOT NULL, HORAS DOUBLE NOT NULL, FECHAINICIO DATE NOT NULL, FECHAFIN DATE NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL,  
CONSTRAINT NOMBRE_PK PRIMARY KEY (NOMBRE),
CONSTRAINT CURSO_PROPIO_FK FOREIGN KEY (CURSOPROPIO_ID) REFERENCES CURSOPROPIO (ID) ON DELETE NO ACTION, 
CONSTRAINT RESPONSABLE_PROFESOR_FK FOREIGN KEY (RESPONSABLE_PROFESOR_DNI) REFERENCES PROFESOR (DNI) ON DELETE NO ACTION);

/*CREACION DE TABLA PROFESOR*/
CREATE TABLE IF NOT EXISTS PROFESOR (DNI VARCHAR(45) NOT NULL, NOMBRE VARCHAR(45) NOT NULL, APELLIDOS VARCHAR(45) NOT NULL, DOCTOR BOOL NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL,
CONSTRAINT NOMBRE_PK PRIMARY KEY (NOMBRE));

/*CREACION DE TABLA PROFESORUCLM*/
CREATE TABLE IF NOT EXISTS PROFESORUCLM (PROFESOR_DNI VARCHAR(45) NOT NULL, CATEGORIAPROFESOR VARCHAR(45) NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL, 
CONSTRAINT PROFESOR_UCLM_DNI_PK PRIMARY KEY(DNI),
CONSTRAINT PROFESOR_UCLM_DNI_FK FOREIGN KEY (PROFESOR_DNI) REFERENCES PROFESOR (DNI) ON DELETE NO ACTION, 
CONSTRAINT CENTRO_PROPIO_FK FOREIGN KEY (CENTRO_NOMBRE) REFERENCES CENTRO (NOMBRE) ON DELETE NO ACTION);

/*CREACION DE TABLA PROFESOREXTERNO*/
CREATE TABLE IF NOT EXISTS PROFESOREXTERNO (PROFESOR_DNI VARCHAR(45), TITULACION VARCHAR(45) NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL, 
CONSTRAINT PROFESOREXTERNO_PK PRIMARY KEY (DNI), 
CONSTRAINT PROFESOR_UCLM_DNI_FK FOREIGN KEY (PROFESOR_DNI) REFERENCES PROFESOR (DNI) ON DELETE NO ACTION);

/*CREACION DE TABLA ESTUDIANTE*/
CREATE TABLE IF NOT EXISTS ESTUDIANTE (DNI VARCHAR(45) NOT NULL, NOMBRE VARCHAR(45) NOT NULL, APELLIDOS VARCHAR(45) NOT NULL, TITULACION VARCHAR(45) NOT NULL, CUALIFICACION VARCHAR(45) NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL,
CONSTRAINT NOMBRE_PK PRIMARY KEY (NOMBRE));

/*CREACION DE TABLA CENTRO*/
CREATE TABLE IF NOT EXISTS CENTRO (NOMBRE VARCHAR(45) NOT NULL, LOCALIZACION VARCHAR(45) NOT NULL, ATTRIBUTE INT NOT NULL, FECHACREACION DATE NOT NULL, FECHAACTUALIZACION DATE NOT NULL,
CONSTRAINT NOMBRE_PK PRIMARY KEY (NOMBRE));

